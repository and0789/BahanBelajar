package org.itc.exercise01;

public class PersegiPanjang {
    public static void main(String[] args) {
        int panjang = 10;
        int lebar = 20;

        int luas = panjang * lebar;

        System.out.println("Luas persegi panjang " +
                "dengan panjang " + panjang + " cm " +
                "dan lebar " + lebar + " cm adalah " +
                luas + " cm2");
    }
}
